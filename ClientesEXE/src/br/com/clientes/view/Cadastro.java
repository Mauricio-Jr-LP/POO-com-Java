        public static String listagem(CaixaDAO cx){
        ArrayList<Caixa> lista = cx.consulta();
        String texto=""; 
        if(lista.isEmpty()) texto = "Não é possível listar!";
        int saldo =0;
        for (int a=0; a< lista.size();a++){
            saldo += lista.get(a).getValor();
            texto += lista.get(a).toString()+" Saldo: R$"+saldo+"\n";
        }
        return texto;
    }

