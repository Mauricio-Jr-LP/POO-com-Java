    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        int c = Integer.parseInt(txtReg.getText());
        Caixa cx = controle.buscar(c);
        if (cx!=null){
            txtPainel.setText("Resultados encontrados com sucesso!");
            txtReg.setText(""+cx.getRegistro());
            txtDesc.setText(cx.getDescricao());
            if(cx.getTipo()=='E'){
            txtValor.setText(Float.toString(cx.getValor()));
            btnEntrada.setSelected(true);
            btnSaida.setSelected(false);
            } else {
              txtValor.setText(Float.toString(cx.getValor()*(-1)));
              btnSaida.setSelected(true);
              btnEntrada.setSelected(false);
            }
        } else {
            txtPainel.setText("Nenhum dado encontrado!");
            txtDesc.setText("");
            txtValor.setText("");
            btnEntrada.setSelected(false);
            btnSaida.setSelected(false);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if (Integer.parseInt(txtValor.getText())<0)
        {
            JOptionPane.showMessageDialog(null, "DIGITE VALORES MAIORES QUE "
                + "ZERO, MESMO SE FOR VALORES DE SAÍDA!","ERRO", JOptionPane.ERROR_MESSAGE);
        }else if (btnEntrada.isSelected() && btnSaida.isSelected()){
            JOptionPane.showMessageDialog(null, "Selecione apenas uma opção !","ERRO", JOptionPane.ERROR_MESSAGE);
        } else {        
        Caixa cx = new Caixa();
        int     r = Integer.parseInt(txtReg.getText());
        String  d = txtDesc.getText();
        char  t;
        float v;
        if (btnEntrada.isSelected()){
        v = Float.parseFloat(txtValor.getText());
        t = 'E'; 
        } else {
        v = Float.parseFloat(txtValor.getText()) * (-1);
        t = 'S';
        }
        cx.setRegistro(r); 
        cx.setDescricao(d);
        cx.setValor(v);
        cx.setTipo(t);
        if (controle.incluir(cx))
            JOptionPane.showMessageDialog(null,
                    "Dados Gravados com Sucesso");
        else
            JOptionPane.showMessageDialog(null,
                    "Erro na Gravação dos Dados");
            txtReg.setText("");
            txtDesc.setText("");
            txtValor.setText("");
            btnEntrada.setSelected(false);
            btnSaida.setSelected(false);
        }
        
    }//GEN-LAST:event_btnGravarActionPerformed

